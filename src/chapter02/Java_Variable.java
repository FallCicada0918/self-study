package chapter02;

public class Java_Variable
{
    public static void main(String[] args) {
        //TODO　变量
        // 所谓的变量，其实就是改变的向量存储
        // 1.变量得声明
        //  数据类型 变量的名称
        String name;
        // 2.变量得赋值
        //  变量名称=数据
        name = "zhangsan";
        // 3.变量得使用
        //  直接访问变量得名称即可
        System.out.println(name);
        // 將變量的聲明和賦值在一行代碼中完成
        String Username = "lisi";
        System.out.println(Username);
        // 在使用變量之前，必須要給變量賦值，將這個操作稱之爲變量的初始化

    }




}
